/*
select * from 標的

exec .dbo.up_傳入區域_更新標的  1 
 

*/
create PROCEDURE up_傳入區域_更新標的 
( 
@num   bigint 
)
as
begin
--------------------------------------------------------------------------------------------------------
update 標的  set 區域 = 區域.區域, 
                 業務 = 區域.業務
from 區域
where 標的.num  =  區域.num 
and   區域.num  =  @num 

 
--------------------------------------------------------------------------------------------------------
END

GO


